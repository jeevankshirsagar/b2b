class UsersForm extends Component {
  iniValues = () => {
    return IniValues(usersFields, this.props.record || {});
  };

  formValidations = () => {
    return FormValidations(usersFields, this.props.record || {});
  };

  handleSubmit = (values) => {
    const { id, ...data } = PreparedValues(usersFields, values || {});
    this.props.onSubmit(id, data);
  };

  title = () => {
    if (this.props.isProfile) {
      return "Edit My Profile";
    }

    return this.props.isEditing ? "Edit users" : "Add users";
  };

  renderForm() {
    const { saveLoading } = this.props;

    return (
      <Widget title={<h4>{this.title()}</h4>} collapse close>
        <Formik
          onSubmit={this.handleSubmit}
          initialValues={this.iniValues()}
          validationSchema={this.formValidations()}
          render={(form) => {
            return (
              <form onSubmit={form.handleSubmit}>
                {/* <ProductsAutocompleteFormItem
                  name={"wishlist"}
                  schema={usersFields}
                  showCreate={!this.props.modal}
                  mode="multiple"
                /> */}

                <InputFormItem name={"firstName"} schema={usersFields} />

                <InputFormItem name={"lastName"} schema={usersFields} />

                <InputFormItem name={"phoneNumber"} schema={usersFields} />

                <InputFormItem name={"email"} schema={usersFields} />

                <InputFormItem name={"bname"} schema={usersFields} />

                <InputFormItem name={"baddress"} schema={usersFields} />

                <InputFormItem name={"cin"} schema={usersFields} />

                <InputFormItem name={"gst"} schema={usersFields} />

                <RadioFormItem name={"balance"} schema={usersFields} />

                <DatePickerFormItem
                  name={"duedate"}
                  schema={usersFields}
                  showTimeInput
                />

                <InputFormItem name={"aadhar"} schema={usersFields} />

                <ImagesFormItem
                  name={"aadhar_url"}
                  schema={usersFields}
                  path={"users/avatar"}
                  fileProps={{
                    size: undefined,
                    formats: undefined,
                  }}
                  max={undefined}
                />

<ImagesFormItem
                  name={"aadhar_back_url"}
                  schema={usersFields}
                  path={"users/avatar"}
                  fileProps={{
                    size: undefined,
                    formats: undefined,
                  }}
                  max={undefined}
                />

                <InputFormItem name={"pan"} schema={usersFields} />

                <ImagesFormItem
                  name={"pan_url"}
                  schema={usersFields}
                  path={"users/ausers/avatarvatar"}
                  fileProps={{
                    size: undefined,
                    formats: undefined,
                  }}
                  max={undefined}
                />

                <SwitchFormItem name={"disabled"} schema={usersFields} />

                <ImagesFormItem
                  name={"avatar"}
                  schema={usersFields}
                  path={"users/avatar"}
                  fileProps={{
                    size: undefined,
                    formats: undefined,
                  }}
                  max={undefined}
                />

                <div className="form-buttons">
                  <button
                    className="btn btn-primary"
                    disabled={saveLoading}
                    type="button"
                    onClick={form.handleSubmit}
                  >
                    Save
                  </button>{" "}
                  <button
                    className="btn btn-light"
                    type="button"
                    disabled={saveLoading}
                    onClick={form.handleReset}
                  >
                    Reset
                  </button>{" "}
                  <button
                    className="btn btn-light"
                    type="button"
                    disabled={saveLoading}
                    onClick={() => this.props.onCancel()}
                  >
                    Cancel
                  </button>
                </div>
              </form>
            );
          }}
        />
      </Widget>
    );
  }

  render() {
    const { isEditing, findLoading, record } = this.props;

    if (findLoading) {
      return <Loader />;
    }

    if (isEditing && !record) {
      return <Loader />;
    }

    return this.renderForm();
  }
}

export default UsersForm;
